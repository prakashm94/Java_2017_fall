    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:

        if (txtVaccineName.getText().equals("") || !system.getConditionsList().isUniqueCondition(txtVaccineName.getText())) {
            if (!system.getConditionsList().isUniqueCondition(txtVaccineName.getText())) {
                conErrLbl.setText("This condition already exists.");
            }
            if (txtVaccineName.getText().equals("")) {
                conErrLbl.setText("Cant leave condition blank.");
            }
            txtVaccineName.setBorder(new LineBorder(Color.red));
        } else {
            conErrLbl.setText("");
            txtVaccineName.setBorder(new LineBorder(Color.black));
            Conditions c = system.getConditionsList().addCondition();
            c.setDiseaseName(txtVaccineName.getText());
            populateDiseaseTable();
            txtVaccineName.setText("");
        }
    }//GEN-LAST:event_btnNewActionPerformed

