    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDisease.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to delete the account", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            Conditions p = (Conditions) tblDisease.getValueAt(selectedRow, 0);

            system.getConditionsList().removeCondition(p);
            JOptionPane.showMessageDialog(null, "You have successfully deleted the condition");
            populateDiseaseTable();
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

