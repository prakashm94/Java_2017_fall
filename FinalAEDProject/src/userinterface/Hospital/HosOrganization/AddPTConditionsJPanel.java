        // TODO add your handling code here:
        
        Conditions c= (Conditions) comboDisease.getSelectedItem();
        System.out.println("userinterface.Hospital.HosOrganization.AddPTConditionsJPanel.jButton1ActionPerformed()");
        if(organization.getPtConditions()==null){
             System.out.println("hi");
            organization.setPtConditions(new PTConditionsDirectory());
        }
        
        ArrayList<String> validatePT = new ArrayList<>();
        for(Conditions cond : organization.getPtConditions().getConditionList())
        {
            validatePT.add(cond.getDiseaseName());
            
        }
        if(validatePT.contains(c.getDiseaseName()))
            JOptionPane.showMessageDialog(null,"This condition has already been added as a physical therapy condition");
        else{
        Conditions cond = organization.getPtConditions().addCondition();
        
        cond.setDiseaseName(c.getDiseaseName());

         
       populateTable();
        
        }
