public void populateTable()
{
     DefaultTableModel dtm= (DefaultTableModel) ptconditionTable.getModel();
     dtm.setRowCount(0);
       for (Conditions con : organization.getPtConditions().getConditionList()) {
        
           Object[] row = new Object[2];
           row[0]= con;
           row[1]=con.getDiseaseName();
           dtm.addRow(row);
       }
}

