    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // TODO add your handling code here:
        p.setOpioidName(opNametxt.getText());
        p.setMmeMultiplier(Float.parseFloat(mmeTxt.getText()));
        JOptionPane.showMessageDialog(null, "Updated.");
    }//GEN-LAST:event_confirmBtnActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        
         Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageOpioidsJPanel manageAccountJPanel = (ManageOpioidsJPanel) component;
        manageAccountJPanel.populateVaccineTable();
        
        
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed

    private void delConBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delConBtnActionPerformed
        // TODO add your handling code here:
        p.getConditionList().remove((Conditions)comboDisease.getSelectedItem());
        populateFields();
        JOptionPane.showMessageDialog(null, "Removed condition succesfully");
    }//GEN-LAST:event_delConBtnActionPerformed
void populateFields()
{
    opIDtxt.setText(p.getOpioidId());
    opNametxt.setText(p.getOpioidName());
    opConListTxt.setText(p.getConditionList().toString());
    mmeTxt.setText(String.valueOf(p.getMmeMultiplier()));
    
    
}


   
    public void populatecombo(){
        for (Conditions disease : system.getConditionsList().getConditionList()) {
            comboDisease.addItem(disease);
        }
    }


