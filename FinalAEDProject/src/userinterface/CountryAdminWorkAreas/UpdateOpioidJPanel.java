    private void addConditionBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addConditionBtnActionPerformed
        // TODO add your handling code here:
        
            if(p.getConditionList().contains((Conditions)comboDisease.getSelectedItem()))
            JOptionPane.showMessageDialog(null,"Cannot add multiple instances of same condition to one opioid");
            else
            {

                p.getConditionList().add((Conditions)comboDisease.getSelectedItem());

                JOptionPane.showMessageDialog(null, "You have successfully added condition to the opioid "+p);
                populateFields();
            }
        

    }//GEN-LAST:event_addConditionBtnActionPerformed

