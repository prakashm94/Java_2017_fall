    public void populatePersonTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblVaccine.getModel();
        dtm.setRowCount(0);
        for (Person vaccine : cNetwork.getPersonDir().getPersonList()) {

            Object[] row = new Object[2];
            row[0] = vaccine;
            row[1] = vaccine.getPersonId();
            dtm.addRow(row);
        }
    }

