    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblVaccine.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the row to delete the account", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            Person p = (Person) tblVaccine.getValueAt(selectedRow, 0);
            for (Person per : cNetwork.getPersonDir().getPersonList()) {
                for (NetworkState ns : cNetwork.getNetworkStateList()) {
                    for (Enterprise e : ns.getEnterpriseDirectory().getEnterpriseList()) {
                        for (Organization or : e.getOrganizationDirectory().getOrganizationList()) {
                            if (or instanceof HospitalOrganization) {
                                for (Patient re : ((HospitalOrganization) or).getHosPatientDir().getPatientList()) {
                                    if (re.getUniquePatientId() == p.getPersonId()) {
                                        ((HospitalOrganization) or).getHosPatientDir().getPatientList().remove(re);
                                    }
                                }
                            }

                        }
                    }
                }

            }
            cNetwork.getPersonDir().removePerson(p);

            JOptionPane.showMessageDialog(null, "You have successfully deleted the account");
            populatePersonTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnnewPersonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnewPersonActionPerformed
        // TODO add your handling code here:

        boolean existing_person = false;
        if (txtpatientid.getText().equals("")) {
            txtpatientid.setBorder(new LineBorder(Color.red));
            idErrLbl.setText("Person ID cannot be empty.");
            return;
        }
        for (Person per : cNetwork.getPersonDir().getPersonList()) {
            if (txtpatientid.getText().equalsIgnoreCase(per.getPersonId())) {
                txtpatientid.setBorder(new LineBorder(Color.red));
                idErrLbl.setText("Person identification already exists.");
                return;
            }
        }
        if (lattxt.getText().equals("")) {
            lattxt.setBorder(new LineBorder(Color.red));
            idErrLbl.setText("Must set location");
            return;
        }
        if (personNameTxt.getText().equals("")) {
            personNameTxt.setBorder(new LineBorder(Color.red));
            nameErrLbl.setText("Name cannot be empty.");
        } else {
            personNameTxt.setBorder(new LineBorder(Color.black));
            nameErrLbl.setText("");
            txtpatientid.setBorder(new LineBorder(Color.black));
            idErrLbl.setText("");
            for (Person p : cNetwork.getPersonDir().getPersonList()) {
                if (p != null) {
                    if (p.getPersonId() == null || !p.getPersonId().equals(txtpatientid.getText())) {
                        Person v = cNetwork.getPersonDir().addPerson();
                        v.setPersonName(personNameTxt.getText());
                        v.setPersonId(txtpatientid.getText());
                        v.setLatitude(Double.parseDouble(lattxt.getText()));
                        v.setLongitude(Double.parseDouble(longtxt.getText()));
                        existing_person = true;
                        break;
                    } else {
                        existing_person = false;
                    }
                }
            }
            if (cNetwork.getPersonDir().getPersonList().size() == 0) {
                Person v = cNetwork.getPersonDir().addPerson();
                v.setPersonName(personNameTxt.getText());
                v.setPersonId(txtpatientid.getText());
                existing_person = true;
            }
            if (existing_person == false) {
                JOptionPane.showMessageDialog(null, "Person already exists");
            } else {
                JOptionPane.showMessageDialog(null, "Successfully added person");
            }

            populatePersonTable();
        }
    }//GEN-LAST:event_btnnewPersonActionPerformed

