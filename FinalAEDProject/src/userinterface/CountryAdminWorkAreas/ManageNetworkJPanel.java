    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow= networkJTable.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select the row to delete the account", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{

            NetworkState p=(NetworkState) networkJTable.getValueAt(selectedRow, 0);

            for (NetworkState network : cNetwork.getNetworkStateList()) {
                    if(p==network){
                      cNetwork.getNetworkStateList().remove(p);
                        break;
                    }
            }

            JOptionPane.showMessageDialog(null, "You have successfully deleted the state network");
            populateNetworkTable();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

