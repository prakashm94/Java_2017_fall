    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        String name = nameJTextField.getText();
        if(name.equals(""))
        {
             nameJTextField.setBorder(new LineBorder(Color.red));
             errLbl.setText("Please enter a state name.");
             return;
        }
        if(cNetwork.isUniqueState(name))
        {
            nameJTextField.setBorder(new LineBorder(Color.black));
             errLbl.setText("");
        NetworkState network = cNetwork.createAndAddNetwork();
        network.setName(name);

        populateNetworkTable();
        }
        else
        {
            nameJTextField.setBorder(new LineBorder(Color.red));
             errLbl.setText("This State has been registered already");
        }

    }//GEN-LAST:event_submitJButtonActionPerformed

