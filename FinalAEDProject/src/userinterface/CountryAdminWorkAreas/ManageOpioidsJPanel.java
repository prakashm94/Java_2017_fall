    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
           int selectedRow= tblVaccine.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select an opioid", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            if(tblVaccine.getValueAt(selectedRow, 0).toString().equals("Reversal"))
            {
                JOptionPane.showMessageDialog(null, "Cannot update the reversal drug");
                return;
                
            }
             OpioidMedication p=(OpioidMedication) tblVaccine.getValueAt(selectedRow, 0);
      
        UpdateOpioidJPanel manageEnterpriseJPanel = new UpdateOpioidJPanel(userProcessContainer,system, cNetwork,p);
        userProcessContainer.add("manageOpioidsJPanel", manageEnterpriseJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_updateBtnActionPerformed

    public void populateVaccineTable(){
         DefaultTableModel dtm= (DefaultTableModel) tblVaccine.getModel();
       dtm.setRowCount(0);
       for (OpioidMedication vaccine : cNetwork.getOpioidList().getOpioidList()) {
        
           Object[] row = new Object[3];
           row[0]= vaccine;
           row[1]=vaccine.getOpioidId();
           row[2]=vaccine.getConditionList();
           dtm.addRow(row);
       }  
       }
    
    public void populatecombo(){
        for (Conditions disease : system.getConditionsList().getConditionList()) {
            comboDisease.addItem(disease);
        }
    }

