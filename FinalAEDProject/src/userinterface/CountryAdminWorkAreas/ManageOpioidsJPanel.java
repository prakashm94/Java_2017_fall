    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow= tblVaccine.getSelectedRow();
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select the row to delete this opioid", "Warning", JOptionPane.WARNING_MESSAGE);
        }
       
        else{
            
            
             if(tblVaccine.getValueAt(selectedRow, 0).toString().equals("Reversal"))
        {
            System.out.println(tblVaccine.getValueAt(selectedRow, 0));
            JOptionPane.showMessageDialog(null, "Cannot remove reversal drug");
            return;
        }

            OpioidMedication p=(OpioidMedication) tblVaccine.getValueAt(selectedRow, 0);
            
            

            cNetwork.getOpioidList().removeDisease(p);

            JOptionPane.showMessageDialog(null, "You have successfully deleted the account");
            populateVaccineTable();
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

