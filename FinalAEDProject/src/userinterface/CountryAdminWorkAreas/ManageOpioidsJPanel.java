    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        
        String name = txtVaccineName.getText();
        if(comboDisease.getSelectedItem()==null)
        {
             errLbl.setText("There are no conditions available to add.");
             txtVaccineName.setBorder(new LineBorder(Color.red));
             return;
        }
        if(!cNetwork.getOpioidList().isUniqueOpioid(name))
        {
            errLbl.setText("This opioid already exists in our directory!");
             txtVaccineName.setBorder(new LineBorder(Color.red));
             return;
            
        }
        if(txtMME.getText().equals("")){
            errLbl.setText("Please dont leave MME blank.");
             txtMME.setBorder(new LineBorder(Color.red));
             return;
        }
        if(!name.equals(""))
        {
            errLbl.setText("");
             txtVaccineName.setBorder(new LineBorder(Color.black));
             txtMME.setBorder(new LineBorder(Color.black));
            OpioidMedication v= cNetwork.getOpioidList().addDisease();
             v.setOpioidName(txtVaccineName.getText());
             v.setMmeMultiplier(Float.parseFloat(txtMME.getText()));
            ArrayList<Conditions> conList = v.getConditionList();
            conList.add((Conditions)comboDisease.getSelectedItem());
            v.setConditionList(conList);
            
            populateVaccineTable();
            
           
        }
        else{
             errLbl.setText("Please enter a valid opioid name.");
             txtVaccineName.setBorder(new LineBorder(Color.red));
        }
    }//GEN-LAST:event_btnNewActionPerformed

